				<p>Индекс Доу-Джонса применяется при оценке состояния промышленности США, хотя и является одним из крупнейших мировых фондовых индексов. Полное название термина — промышленный индекс Доу-Джонса (Dow Jones Industrial Average).</p>
				<p style="font-weight: bold;">Понятие индекса Доу-Джонса простыми словами</p>
				<p>Впервые данное понятие было озвучено в 1884 году. Его ввели в оборот владелец компании Dow Jones & Company и редактор газеты Wall Street Journal. Первоначальный вариант индекса содержал 11 составляющих (цены акций 8-ми железнодорожных и 2-х производственных фирм, работавших в то время на американском рынке). С 1896 года у данного термина появилась приставка «промышленный».</p>
				<p>Индекс Доу-Джонса считается старейшим торговым инструментом на финансовом рынке.</p>
				<p>Из первоначального состава сохранилась лишь компания General Electric. Все остальные организации либо были преобразованы, либо ликвидированы:</p>
