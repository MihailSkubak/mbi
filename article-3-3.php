<p>Нисходящий треугольник</p>
<p>Нисходящий треугольник имеет горизонтальную нижнюю сторону и понижающуюся верхнюю. Нисходящий треугольник сигнализирует о том, что предложение на рынке превышает спрос, соответственно предсказывает движение рынка вниз.</p>
