<p>Все треугольники, сформировавшиеся в качестве фигур-продолжения имеют характерную черту: основной тренд продолжается до точки вершины треугольника (пересечение линий консолидации), а затем вероятен его перелом.</p>
<p>Вымпел и флаг</p>
<p>Следующие модели — флаги и вымпелы. Встречаются довольно часто в роли коррекции на энергично развивающихся трендах. Типовые конфигурации этих схожих моделей представлены на рисунке. Сразу можно заметить, что они представляют собой некоторую приостановку в развитии тренда. По сути, вымпел — это маленький треугольник. А флаг — незначительный ценовой канал, как будто «одетый» на предшествующий ход графика.</p>
