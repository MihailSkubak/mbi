<p>Двойная вершина</p>
<p>Следующая модель перелома восходящего тренда называется двойной вершиной. Как мы помним, в модели «голова-плечи» второй пик (голова) выше первого. А в случае двойной вершины на восходящей тенденции после очередного пика образуется пик такого же уровня. Так как цена не поднимается выше, тренд не может сохраниться, а затем цена и вовсе падает ниже последнего минимума.</p>
