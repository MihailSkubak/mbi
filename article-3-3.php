<p>Геометрически «голова и плечи» выглядит как три последовательных пика на вершине восходящего графика. Эти пики образуют характерную конфигурацию — левое плечо, голова и правое плечо, опирающиеся на линию шеи. Эта модель динамики рынка очень упрощена, но она наглядно показывает основные детали формирования перелома. Так как на рынок влияет масса различных факторов, то валютный курс часто отклоняется от идеальной формы. Давайте рассмотрим рисунок подробней. Обычно линия шеи в качестве уровня сопротивления обозначается последним пиком перед формированием фигуры. После пробития этого уровня в точке А, цена формирует максимум левого плеча и снижается к точке В. Отскочив от нее вновь, цена формирует еще более высокий максимум головы, и возвращается в точку С. Такое развитие событий подтверждает, что линия шеи, базирующаяся на точках А, В и С, теперь стала уровнем поддержки. Максимум, достигнутый после отскока от точки С, образует правое плечо. Последним штрихом происходит пробитие ценой уровня линии шеи — именно это означает завершение формирования фигуры «голова-плечи» и перелом восходящего тренда. Отметим, что чаще всего после прорыва линии шеи цена возвращается к ней снизу (точка E) и лишь затем окончательно уходит вниз. </p>
