<p>Симметричный треугольник</p>
<p>Самой типичной фигурой продолжения тенденции является треугольник и его разновидности. Один из видов — симметричный треугольник — образуется из двух сходящихся линий консолидации. Поскольку эти линии примерно симметричны, такой треугольник означает равновесие между силами быков и медведей. Тем не менее, в поведении модели есть определенные закономерности. Если предыдущий тренд был восходящим, то симметричный треугольник вероятно пойдет вверх. Если же предшествующий тренд был нисходящим, то треугольник продолжится ходом вниз. </p>
<p>
Чтобы построить треугольник на графике цены, необходимо иметь не менее двух пар точек на уровнях консолидации: две поддержка и две сопротивление. Две прямые стороны треугольника проводятся как раз через эти пары точек.
</p>
<div class="ForImg">
<img src="img/articles/Graphic_33.jpg" alt="33">
</div>
<p>Считается, что произошел прорыв треугольника, если цена закрытия свечи зафиксирована за пределами одной из соответствующих сторон треугольника (в сторону продолжения тенденции). Простое пересечение стороны наверняка даст ложный сигнал. При этом после прорыва график обычно возвращается к пробитой стороне, а потом уже цена уходит в направлении продолжения предшествующего тренда. Также заметим, что чрезвычайное приближение цены к вершине треугольника ведет к неопределенности в развитии фигуры и часто снижает вероятность продолжения тенденции.</p>
<p>Восходящий треугольник</p>
<p>Следующая модель — восходящий треугольник — имеет горизонтальную верхнюю границу и восходящую нижнюю. Такое расположение линий означает, что спрос на рынке более силен, чем предложение. Покупатели стараются использовать каждый откат цен вниз для открытия позиций на покупку, поэтому уровни откатов становятся с каждым разом все выше. Следовательно, восходящий треугольник предсказывает движение рынка вверх.</p>
