				<h2 style=" font-size: 30px;  text-align: center; padding-bottom: 20px;">Графический анализ </h2>
				<p>Графический анализ</p>
				<p>Мы рассмотрели фундаментальный и технический анализы, также изучили базовые понятия технического анализа: линии поддержки и сопротивления, линии тренда и канала. За долгие годы наблюдения за ходом цен на валютном рынке, было замечено, что различные комбинации этих линий образуют типовые графические образы — фигуры технического анализа. По контуру этих фигур можно понять, какие чувства движут большинством участников рынка. А так как они регулярно появляются на графиках, многие трейдеры традиционно используют их для прогнозирования будущего курса валют.</p>
				<p>В графическом анализе все фигуры принято разделять на два класса:</p>
				<ul>
					<li>разворотные фигуры (фигуры перелома тенденции), которые возникают, когда в развитии рынка намечается смена направления, разворот основной тенденции;</li>
					<li>фигуры продолжения тенденции, указывающие, что основная тенденция рынка не будет меняться и рынок к ней вернется через некоторое время.</li>
				</ul>
				<p>Мы начнем с разворотных фигур.</p>
				<p>Голова-плечи</p>
				<p>Первая и самая важная модель перелома «Голова-плечи». Типовая конфигурация данной фигуры на вершине рынка представлена на рисунке:</p>
