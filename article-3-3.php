<p>Примечательно, что после прорыва вымпела или флага график проходит по тренду то же расстояние, что имело место до фигуры. А еще можно заметить, что фигуры являются более достоверными, когда ориентированы против предшествующего тренда. В заключение — одно «но» (как же без него): если модели не противостоят тренду, а строятся в его направлении, то скорее они свидетельствуют о возможном развороте. </p>
