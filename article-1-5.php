<p>Так котировка GBP/USD = 1.4386/89 показывает, что покупаем у брокера GBP за USD и расплачиваемся по Ask 1.4389, а продаем Bid 1.4386. </p>
<p>
Цена по Bid меньше цены Ask. Разница между ценами — это спред, то есть это комиссия брокера за открытие позиции. Спред бывает фиксированным (постоянным) или плавающим. Плавающий спред меняется со временем. Значение спреда зависит от внутренних условий брокера и от торговой активности конкретной валютной пары. </p>
<p>
	Спред рассчитывается по формуле:
</p>

<h2 style="text-align: center;">Spread = Ask – Bid</h2>
<p>В паре валют GBP/USD спред равняется 0.0003 (=1.4389 – 1.4386) то есть 3 пипса. Пипс/пункт — это самое маленькое значение колебания валютного курса. </p>
<p>
	У GBP/USD один пункт — это изменение последнего знака после запятой на 1. Так, изменение с 1.4386 до 1.4387, или с 1.4386 на 1.14385. Если котировка изменяется на 100 пипсов — это большая фигура.
</p>
<p>
	На сегодняшний день большинство пар валют (GBP/USD, EUR/USD, USD/CHF) имеют десятитысячное котирование, то есть один пункт составляет 0.0001. Только USD/JPY и валютные пары с участием JPY имеют котирование до сотой доли 0.01. Но сейчас более точное котирование является предпочтительным. Поэтому можно встретить котировки 1.24858 по паре валют EUR/USD; 112.972 по паре доллар/йена. При более точных котировках один пункт будет равен изменению последней цифры котировки: 0.00001 и 0.001 соответственно.
</p>
<p>
	При открытии любой позиции обязательно необходимо определить ее объем или размер.
</p>
 <p>
Размер контракта указывается в специальных единицах измерения — лотах.
</p>
<h2 style="text-align: center;">1 лот = 100 000 базовой валюты</h2>
<p>Базовая валюта занимает первое место в записи валютной паре. </p>
<p>
Поэтому если трейдер открывает сделку объемом 1 лот по USD/JPY, размер контракта составляет 100 000 долларов. Но абсолютно не обязательно торговать на валютном рынке целым лотом, лот можно дробить и открывать сделки не полным лотом:
</p>
<ul>
	<li>0.01 лота равняется 1 000 базовой валюты,</li>
	<li>0.2 лота = 20 000 базовой валюты,</li>
	<li>2.1 лота = 210 000 базовой валюты,</li>
	<li>3 лота = 300 000 базовой валюты.</li>
</ul>
<p>Именно от размера контракта зависит будущий убыток или прибыль в сделке. От размера контракта рассчитывается стоимость одного пипса при изменении котировки. </p>
<p>
	Стоимость пункта считается в котируемой валюте (она находится второй в паре валют).
</p>
<p>
Стоимость пункта рассчитывается следующим образом:
</p>
<h2 style="text-align: center;">Стоимость пункта / пипса в котируемой валюте = доля пункта * лот</h2>
<p>Если позиция открыта 1 лотом (валютная пара GBP/USD), то цена пипса = 100 000 * 0.0001 = 10 USD. Если открыть позицию объемом 0.03 лота, то цена пипса составит 3 000 * 0.0001 = 0.3 USD. Поэтому прибыль или убыток позиции будет зависеть от объема лота. </p>
<p>
	Если сделка объемом в 1 лот открыта (валютная пара EUR/JPY), то цена пипса = 100 000 * 0.01 = 1 000 JPY. Если вы хотите посчитать JPY в долларах, нужно разделить на текущий курс USD/JPY (так как 1 иена меньше 1 доллара, мы делим). Получаем, что 1 000 JPY / 112.97 = 8,85 USD.
</p>
 <p>
Мы разобрались с объемом сделки и ценой пипса, теперь нам надо посмотреть, как торговать на валютном рынке Форекс. Посмотрим на график EUR/USD, здесь наблюдается рост EUR к USD. 
</p>
