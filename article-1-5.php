<p>Как мы помним, значение валютного курса в данное время является валютной котировкой. Всегда существует две валютные котировки: цена покупки Ask или Offer (по ней трейдер покупает базовую валюту у брокера) и цена продажи Bid или Last (по ней трейдер продает базовую валюту у брокера). </p>
<p>На рисунке показано, как котировки отображаются в терминале:
</p>
