<p>Трейдер может принять решение о сокращении убытков и закрыть позицию. При таком стечении обстоятельств трейдер получит за 20 000 евро: 20 000 EUR * 1.1151 = 22 302 USD. </p>
<p>
	В итоге мы наблюдаем несоответствие между ценами продажи и покупки: 22 302 – 22 342 = – 40 USD.
</p>
<p>Отрицательная разница цен, к сожалению, является убытком трейдера.</p>
<p>
Брокер не виноват в некорректном прогнозе трейдера, поэтому брокер возвращает себе после закрытия позиции 20 000 евро. Убыток трейдера составит 40 USD и спред (2 пипса по EUR/USD), этот убыток убирается с торгового счета трейдера. Поэтому депозит теперь будет составлять 120 USD.
</p>
<h2 style="text-align: center;">Средства (equity) = сумма на депозите + плавающая прибыль - плавающий убыток.</h2>
<p>Если бы трейдер не закрыл сделку, а валютный курс бы снижался дальше, то убытки бы росли пропорционально и уменьшали объем средств (equity) на торговом счете 100 .. 80 .. 60 .. 40... Чтобы контролировать уровень убытков трейдера, брокер считает объем и уровень залога (маржи).</p>
<h2 style="text-align: center;">Уровень маржи = средства / залог * 100%</h2>
<p>Если уровень маржи становится меньше 10%, то срабатывает автоматический приказ брокера и самая убыточная сделка будет закрыта по стоп-ауту (Stop Out). Брокер исполняет данный приказ, чтобы сохранить деньги, одолженные трейдеру. </p>
<p>
В рассматриваемом примере, стоп-аут наступит, когда средства на торговом счете станут меньше 11.2 USD (10% от маржи, которая составляет 111,74 USD).
</p>
<p>Вывод: торгуя на валютном рынке, трейдер пользуется своим и заемным капиталом, причем собственный капитал составляет обычно от 0.002 до 1 % от общего капитала для торговли, остальные средства — это займ у брокера. Так, используя возможности маржинальной торговли, трейдер открывает сделки на сумму, превышающую его наличные средства, и может заработать больше прибыли.</p>
