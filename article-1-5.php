<p>Поэтому мы закрываем позицию, чтобы получить и зафиксировать прибыль. В соответствии с правилами маржинальной торговли каждая сделка, находящаяся в рынке, должна быть закрыта противоположной сделкой такого же объема. При закрытии сделки брокер продает 20 000 EUR за USD, по выросшему курсу 1.1371 (Bid). Поэтому за 20 000 EUR, мы получаем 20 000 EUR * 1.1371 = 22 742 USD. </p>
<p>
	Брокер забирает себе 20 000 евро, предоставленные ранее, и маржинальный залог в размере 111.74 USD становится доступен трейдеру для дальнейших действий. Прибыль, полученная с помощью разницы цены продажи и покупки также полностью принадлежит трейдеру 22 742 – 22 342 = 400 USD.
</p>
<p>
	Это прибыль трейдера, которая зачисляется на торговый счет после закрытия сделки.
</p>
<p>
	В итоге на нашем торговом счете находится 560 USD (160 USD торгового депозита и 400 USD прибыли). Ваша прибыль будет отражаться в валюте выбранного счета.
</p>
 <p>
Если торговый прогноз оказался ошибочным и цена, достигнув уровня 1.1171 (уменьшилась на 20 пипсов).
</p>
