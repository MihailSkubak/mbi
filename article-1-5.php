				<p>Поэтому, если мы рассматриваем пару USD/JPY, то при увеличении стоимости USD к JPY мы будем открывать длинную позицию, при уменьшении стоимости USD к JPY — короткую. Эта закономерность прослеживается на всех парах валют, поэтому можно получить прибыль, торгуя на понижении или на росте пары. </p>
